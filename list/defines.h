#ifndef DS_DEFINES_H
#define DS_DEFINES_H

#include <stdint.h>
#include <stdbool.h>

/** @brief A pointer to a generic block of memory */
typedef void*         vptr;

/** @brief A 8 bits unsigned integer */
typedef uint8_t       u8;
/** @brief A 16 bits unsigned integer */
typedef uint16_t      u16;
/** @brief A 32 bits unsigned integer */
typedef uint32_t      u32;
/** @brief A 64 bits unsigned integer */
typedef uint64_t      u64;

/** @brief A 8 bits signed integer */
typedef int8_t        i8;
/** @brief A 16 bits signed integer */
typedef int16_t       i16;
/** @brief A 32 bits signed integer */
typedef int32_t       i32;
/** @brief A 64 bits signed integer */
typedef int64_t       i64;



#endif